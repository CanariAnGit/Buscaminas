    private void rellenarComboBox(){
        for(Usuario j : datos.getUsuarios().getListaUsuarios()){  //Recorre los usuarios registrados.
            if(datos.getJ1().equals(j)==false){  //Se guardan los usuarios que no son el actual.
                jugadoresBoton.addItem(j);  //Añade el jugador al botón.
            }
        }
    }

