    private void comparacionBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comparacionBotonActionPerformed
        Jugador jugadorOtro=(Jugador)jugadoresBoton.getSelectedItem();  //Recibe el nombre del jugador seleccionado.;
        datos.getJ1().crearComparativa(jugadorOtro);  //Crea la comparativa entre los dos jugadores. 
        if(jugadorOtro.getPartidasJugadas().isEmpty()){  //Si la lista de partidas del otro jugador est vacía.
            areaEscritura.setText("Este usuario aun no ha jugado.\n");
        }else{
            areaEscritura.setText(datos.getJ1().getComparativa().compararJugadores()); 
        }       
    }//GEN-LAST:event_comparacionBotonActionPerformed

