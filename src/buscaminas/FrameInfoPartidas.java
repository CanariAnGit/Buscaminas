    private void verJugadoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verJugadoresActionPerformed
        Partida p=(Partida) comboPartidas.getSelectedItem();  //Recibe la partida seleccionada por el usuario.
        areaSalida.setText(p.getJugadorActual().verPerfil() + "\n" + p.getJugadorEsperando().verPerfil() + "\n"); 
        //Escribe los nombres y estadisticas de los jugadores.
    }//GEN-LAST:event_verJugadoresActionPerformed

    private void getMarcadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getMarcadorActionPerformed
        Partida p=(Partida) comboPartidas.getSelectedItem();
        Marcador marcador=p.getMarcador();
        areaSalida.setText("El marcador quedo:\n" + marcador.getMinasJ1() + " minas para " +
                p.getJugadorActual().getUsuario() + "\n" + "y " + marcador.getMinasJ2() + 
                " minas para " + p.getJugadorEsperando().getUsuario() + "\n");
    }//GEN-LAST:event_getMarcadorActionPerformed

    private void tableroMovimientosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tableroMovimientosActionPerformed
        Partida p=(Partida) comboPartidas.getSelectedItem();
        areaSalida.setText(p.imprimirTablero() + "\n" + p.printMovimientos() + "\n" + "Tambien te lo he dejado en un fichero dentro de la carpeta del proyecto.\n");  //Imprime el tablero en le ventana.
        p.getAlmacenPartidas().tablero_y_movimientos(p, datos.getFicheroTableroMovimientos());
    }//GEN-LAST:event_tableroMovimientosActionPerformed

